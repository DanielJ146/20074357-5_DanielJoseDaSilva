        <h3>Igredientes</h3>
        <ul>
            <li>3 Ovos</li>
            <li>1 Lata de Leite Condensando</li>
            <li>300ml de Leite (ou a mesma medida da lata de leite condensado)</li>
            <li>2 Colheres (sopa) de Karo (Glucose de Milho)</li>
        </ul>
        <h3>Modo de Preparo</h3>
        <ol>
            <li>Em um liquidificador adicione o leite, leite condensado e os ovos e bata. (Se desejar um pudim solido bata apensas para misturar, para um pudim furadinho bata bem).</li>
            <li>Em uma forma para pudim (cone no meio) adicione o karo e espalhe com uma espatula por toda a forma.</li>
            <li>Adicione o creme batido e cubra a forma com papel alumínio.
            </li>
            <li>Leve para assar em forno pré aquecido á 180ºC por 40 minutos e depois por mais 20 minutos sem o papel alumínio. (Esse tempo pode variar de forno pra forno).</li>
            <li>Leve para gelar (o ideal é que fique bem gelado antes de desenformar)</li>
            <li>Para desenformar aqueça a forma no fogo rapidamente, passe uma faquinha em toda volta, coloque um prato por cima da forma e vire.</li>
        </ol>
