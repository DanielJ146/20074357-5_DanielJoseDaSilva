        <h3>Modo de Preparo</h3>
        <ol>
            <li>Em uma panela, coloque o Chocolate e a manteiga e leve ao fogo em banho-maria, até obter um creme homogêneo.</li>
            <li>Adicione o açúcar mascavo e o açúcar e mexa até estar bem dissolvido.</li>
            <li>Em um recipiente, bata os ovos com a essência de baunilha e misture ao creme de chocolate.</li>
            <li>Adicione a farinha de trigo, o bicarbonato e as nozes.</li>
            <li>Despeje a massa em uma forma retangular (23 cm x 32 cm), untada com manteiga e polvilhada com farinha de trigo e leve ao forno médio (180°C), preaquecido, por cerca de 20 minutos</li>
            <li>Desenforme e deixe esfriar. Corte em quadrados e sirva.</li>
