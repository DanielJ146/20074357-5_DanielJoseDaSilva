        <h3>Igredientes</h3>
        <ul>
            <li>1 litro de LEITE INTEGRAL TIPO A (Quanto mais gordo e fresco for o leite, mais gostoso vai ficar o doce de leite)</li>
            <li>1 + 1/2 xícara de AÇÚCAR CRISTAL (ou apenas 1 xícara se preferir menos doce)</li>
            <li>2 colheres de sopa de GLUCOSE (reduz a probabilidade do doce cristalizar enquanto você mexe)</li>
            <li>1 pitada de SAL</li>
            <li>1 FAVA DE BAUNILHA (ou extrato no final do preparo)</li>
            <li>1/4 colher de chá de BICARBONATO DE SÓDIO (previne que a proteína do leite coagule, o que no final resulta em um doce de leite com uma consistência mais cremosa)</li>
        </ul>
        <h3>Modo de Preparo</h3>
        <ol>
            <li>Junte todos os ingredientes, menos o bicarbonato, em uma panela grande e alta.</li>
            <li>Cozinhe em fogo médio/alto, mexendo para que o açúcar se dissolva, até que comece a formar uma espuma leve por cima do leite. Então abaixo o fogo para o mínimo e junte o bicarbonato. Mexa até que o leite pare de subir.</li>
            <li>Continue cozinhando em fogo bem baixo, mexendo de quando em quando para não grudar. Quando ele já estiver quase na consistência final cozinhe mexendo bastante para que ele reduza mais rapidamente e pegue corpo.</li>
            <li>Quando o leite tiver reduzido para quase 1/4, ou estiver na cor desejada e com consistência de doce de leite, retire da panela e reserve em um pote fechado até esfriar.</li>
        </ol>
